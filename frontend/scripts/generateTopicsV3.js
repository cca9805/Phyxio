function generateTopicsData() {
  const topicsData = {};
  
  // Agregar entrada raíz para la página principal
  topicsData[""] = {
    title: "Física - Ciencia del Universo",
    intro: "La física es la ciencia natural que estudia las propiedades fundamentales de la materia, la energía, el tiempo y el espacio, así como las interacciones entre ellos.",
    cards: [
      {
        tipo: "clasica",
        titulo: "Física Clásica",
        descripcion: "Mecánica, Termodinámica, Óptica, Electromagnetismo...",
        icono: emojiMap['clasica'] || "m_clasica",
        btn: {
          texto: "Explorar",
          clase: "clasica",
          link: "/clasica"
        }
      },
      {
        tipo: "moderna",
        titulo: "Física Moderna",
        descripcion: "Relatividad, Cuántica, Física de partículas...",
        icono: emojiMap['moderna'] || "m_moderna",
        btn: {
          texto: "Explorar",
          clase: "moderna",
          link: "/moderna"
        }
      }
    ],
    additionalContent: ""
  };
  
  // Generar todas las secciones basadas en hierarchyConfig
  for (const [sectionKey, sectionData] of Object.entries(hierarchyConfig)) {
    if (sectionKey === 'main') continue; // Skip main
    if (sectionKey === 'optica') {
      console.log('🔍 Óptica hierarchy entries:', Array.isArray(sectionData) ? sectionData.join(', ') : sectionData);
    }
    
    // Generar la sección padre
    topicsData[sectionKey] = generateSection(sectionKey, sectionData, 
      ['clasica', 'moderna', 'mecanica', 'termodinamica', 'electromagnetismo'].includes(sectionKey)
    );
    
    // Generar todas las subsecciones si existen
    if (Array.isArray(sectionData)) {
      for (const childSection of sectionData) {
        // Solo generar si no existe ya (evitar duplicados)
        if (!topicsData[childSection]) {
          // Verificar si este childSection tiene sus propios hijos en hierarchyConfig
          const childData = hierarchyConfig[childSection];
          topicsData[childSection] = generateSection(childSection, childData || [], false);
          
          // Si el childSection tiene hijos, generarlos también
          if (Array.isArray(childData)) {
            for (const grandchildSection of childData) {
              if (!topicsData[grandchildSection]) {
                // Verificar si este grandchild tiene sus propios hijos
                const grandchildData = hierarchyConfig[grandchildSection];
                topicsData[grandchildSection] = generateSection(grandchildSection, grandchildData || [], false);
                
                // Si el grandchild tiene hijos, generarlos también (nivel 4)
                if (Array.isArray(grandchildData)) {
                  for (const greatGrandchildSection of grandchildData) {
                    if (!topicsData[greatGrandchildSection]) {
                      topicsData[greatGrandchildSection] = generateSection(greatGrandchildSection, [], false);
                    }
                  }
                }
              }
            }
          }
        }
      }
    }
  }
  
  // Agregar sección 'moderna' si no existe
  if (!topicsData['moderna']) {
    topicsData['moderna'] = generateSection('moderna', [], false);
  }
  
  return topicsData;
}

