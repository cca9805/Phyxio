function capitalizeTitle(str) {
  // Primero buscar en el mapeo de títulos
  if (titleMapping[str]) {
    return titleMapping[str];
  }
  
  // Si no está en el mapeo, generar automáticamente
  return str
    .replace(/[-_]/g, ' ')
    .replace(/\b\w/g, l => l.toUpperCase())
    .replace(/\d+\s+/, match => match); // Mantener números al inicio
}

/**
 * Obtiene el icono para una sección
 * Prioridad: iconMap.generated.json > iconConfig > emojiMap > default
 */
function getIconForSection(sectionKey) {
  // 1. Buscar en iconMap.generated.json (máxima prioridad)
  if (iconMap[sectionKey]) {
    return iconMap[sectionKey];
  }
  
  // 2. Buscar en iconConfig (temas principales sin teoria.jsx)
  if (iconConfig[sectionKey]) {
    return iconConfig[sectionKey];
  }
  
  // 3. Buscar en emojiMap (fallback)
  if (emojiMap[sectionKey]) {
    return emojiMap[sectionKey];
  }
  
  // 4. Default
  return '📚';
}

function generateSection(sectionKey, sectionData, isTopLevel = false) {
  const title = capitalizeTitle(sectionKey);
  const intro = getIntroForSection(sectionKey, isTopLevel);
  
  let cards = [];
  
  if (Array.isArray(sectionData)) {
    // Es una lista de subsecciones
    cards = sectionData.map(subsectionKey => ({
      tipo: subsectionKey,
      titulo: capitalizeTitle(subsectionKey),
      descripcion: getDescriptionForSection(subsectionKey),
      icono: getIconForSection(subsectionKey),
      palette: getPaletteForSection(subsectionKey),
      isTheoryOnly: isTheoryOnlySection(subsectionKey),
      btn: {
        texto: "Explorar",
        clase: `${subsectionKey}-btn`,
        link: generateLinkForSection(sectionKey, subsectionKey)
      }
    }));
  }
  
  return {
    title,
    intro,
    cards,
    additionalContent: getAdditionalContentForSection(sectionKey)
  };
}

