  sections.forEach(([key, data], index) => {
    content += `  "${key}": ${JSON.stringify(data, null, 4)}`;
    if (index < sections.length - 1) {
      content += ',';
    }
    content += '\n';
  });
  
  content += '};\n';
  
  // Fix icon references - replace string references with actual variables
  // PERO solo para referencias PNG, NO para emojis
